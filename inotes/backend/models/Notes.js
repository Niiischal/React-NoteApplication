const mongoose = require('mongoose');

const notesSchema = new Schema({
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    tag: {
        type: String,
        default: "General"
    },
    date: {
        type: Date,
        default: Date.now
    },
  });

// using the schema 
module.exports = mongoose.model('notes', notesSchema)